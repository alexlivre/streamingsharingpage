function renderAssinaturas(dataToRender = assinaturasData) {
    const container = document.getElementById('services-container');
    const cardTemplate = document.getElementById('service-card-template');
    container.innerHTML = ''; // Clear the container

    dataToRender.forEach(assinatura => {
        const card = cardTemplate.cloneNode(true);
        card.style.display = 'flex'; // Show the cloned card
        card.removeAttribute('id'); // Remove the template ID

        card.querySelector('[data-name]').textContent = assinatura.name;
        card.querySelector('[data-value]').textContent = assinatura.value;
        card.querySelector('[data-payment-method]').textContent = assinatura.paymentMethod;
        card.querySelector('[data-status]').textContent = assinatura.status;
        card.querySelector('[data-access]').textContent = assinatura.access;
        card.querySelector('[data-added-date]').textContent = assinatura.addedDate;
        card.querySelector('[data-telegram-link]').href = assinatura.telegramLink;
        card.querySelector('[data-whatsapp-link]').href = assinatura.whatsappLink;

        container.appendChild(card);
    });
}

// Renderiza as assinaturas quando a página carrega
document.addEventListener('DOMContentLoaded', () => {
    renderAssinaturas();
    // Adiciona um listener para a busca (implementaremos em outra tarefa)
    document.getElementById('search-input').addEventListener('input', (e) => {
        const searchTerm = e.target.value.toLowerCase();
        const filteredAssinaturas = assinaturasData.filter(assinatura =>
            assinatura.name.toLowerCase().includes(searchTerm) ||
            assinatura.status.toLowerCase().includes(searchTerm)
            // Você pode adicionar mais campos para busca aqui
        );
        renderAssinaturas(filteredAssinaturas);
    });
});